
public class Calc{

    public Calc(){}

    public int suma(int n1, int n2){
        int resultado = n1 + n2;
        return resultado;
    }

    public int resta(int n1, int n2){
        int resultado = n1 - n2;
        return resultado;
    }

    public int multiplicacion(int n1, int n2){
        int resultado = n1*n2;
        return resultado;
    }
}